/*
 * 题目：求杨辉三角的前 n 行数据，每个数据占 4 列。
 *
 * 你只要记住三句话：
 *   1）每一行的第一个数是 1
 *   2）每一行的最后一个数是 1
 *   3）中间的数 = 上一行“左上”和“正上方”两个数之和
 *
 * 下面我们一步一步来写代码。
 */

#include <stdio.h>

int main(void) {
    /* 第 1 步：声明变量，用来保存要打印的行数 n */
    int n;

    /*
     * 第 2 步：提示用户输入 n，并用 scanf 读进来。
     * 题目给的条件是 n < 10，这里简单提示一下。
     * 注意：在 OJ/自动评测环境里一般不需要 printf 这一句，
     * 如果老师要求严格可以把这句 printf 去掉，只留 scanf。
     */
    printf("请输入行数 n (0 < n < 10): ");
    scanf("%d", &n);

    /*
     * 第 3 步：准备一个二维数组存杨辉三角。
     *
     * a[i][j] 表示：
     *   第 i 行、第 j 列的数字（i 和 j 都从 0 开始数）。
     *
     * 因为 n < 10，所以我们开一个 10×10 的数组就足够了。
     * 用 {0} 把数组里所有元素初始化为 0，方便后面使用。
     */
    int a[10][10] = {0};

    /*
     * 第 4 步：用 for 循环，按照“杨辉三角的规律”把数组里的值算出来。
     *
     * 外层循环：控制“第几行”（下标 i）
     *   i 从 0 到 n-1，一共 n 行。
     */
    for (int i = 0; i < n; i++) {
        /*
         * 4.1 每一行的第一个数是 1
         *     对应数组里的 a[i][0]。
         */
        a[i][0] = 1;

        /*
         * 4.2 每一行的最后一个数也是 1
         *     对应数组里的 a[i][i]。
         *     比如第 3 行（i=2，下标从 0 开始）：
         *       有三个数字：a[2][0], a[2][1], a[2][2]
         *       最后一个就是 a[2][2]。
         *
         * 当 i = 0 时，这一行只有一个数 a[0][0]，前面已经赋值为 1，
         * 再写一次 a[0][0] = 1 也没问题。
         */
        a[i][i] = 1;

        /*
         * 4.3 计算“中间的数字”
         *
         *   对于第 i 行（i >= 2），中间的下标 j 从 1 到 i-1。
         *   公式：a[i][j] = a[i-1][j-1] + a[i-1][j]
         *
         *   举例：
         *     i = 3（第 4 行，下标从 0 算）：
         *       a[3][1] = a[2][0] + a[2][1]
         *       a[3][2] = a[2][1] + a[2][2]
         *
         *   当 i = 0 或 i = 1 时，循环条件 j < i 不成立，
         *   也就是说这一段代码不会执行，刚好这两行也没有“中间的数字”。
         */
        for (int j = 1; j < i; j++) {
            a[i][j] = a[i - 1][j - 1] + a[i - 1][j];
        }
    }

    /*
     * 第 5 步：按要求把前 n 行打印出来。
     *
     * 外层循环：控制打印第几行 i。
     * 内层循环：控制这一行打印到第几列 j。
     */
    for (int i = 0; i < n; i++) {
        /*
         * 本行有 i+1 个数字（下标 0 到 i）。
         */
        for (int j = 0; j <= i; j++) {
            /*
             * "%4d" 的意思是：
             *   打印一个整数，占 4 个字符宽度。
             *   数字不够 4 位时，会自动在前面补空格，
             *   这样每一列可以对齐，看起来更整齐。
             */
            printf("%4d", a[i][j]);
        }
        /*
         * 每打完一行，记得换行。
         */
        printf("\n");
    }

    /*
     * 程序结束，返回 0，表示正常结束。
     */
    return 0;
}

