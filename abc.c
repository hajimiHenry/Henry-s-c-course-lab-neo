#include <stdio.h>

struct Hero {
    int id;
    int hp;
};

int main() {
    // 1. 创建一个有3个英雄的数组
    struct Hero team[3] = {
        {0, 100}, // 英雄0
        {1, 100}, // 英雄1
        {2, 100}  // 英雄2
    };

    // 2. 创建一个指针，指向数组开头
    struct Hero *p = team; 

    // --- 演示不同的访问方式 ---

    // 方式 A: 纯指针操作 (用箭头 ->)
    printf("方式A: ID=%d, HP=%d\n", p->id, p->hp); // 访问第0个

    // 方式 B: 移动指针 (用箭头 ->)
    p++; // 指针指向下一个了！
    printf("方式B: ID=%d, HP=%d\n", p->id, p->hp); // 访问第1个

    // 方式 C: 指针加下标 (用点 .) 【最容易错的地方】
    // 虽然 p 是指针，但 p[2] 意思是"取出第2个家伙"
    // 既然取出了家伙(真身)，就要用点！
    printf("方式C: ID=%d, HP=%d\n", p[1].id, p[1].hp); // 访问第2个 (因为刚才p已经++过一次了，现在的p[1]其实是原来的team[2])

    return 0;
}