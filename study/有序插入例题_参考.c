// 题目名称：有序序列的多次插入（参考实现）
/*
题目描述：
有一个已经按从小到大排好序的整数序列，存放在数组中。
现在要向这个序列中插入若干个新的整数。每插入一个整数，
都要把它放在合适的位置，保证序列始终保持从小到大。
在所有插入操作完成后，输出最终的有序序列。

输入格式：
第一行输入一个整数 n，表示初始序列中元素的个数。
第二行输入 n 个按从小到大排好序的整数。
第三行输入一个整数 q，表示接下来要插入的次数。
接下来 q 行，每行输入一个整数 x，表示一次要插入的元素。
这些 x 要依次插入，每插入一次，都要在当前序列中保持有序。

输出格式：
输出一行，为所有插入完成后的有序序列。
相邻两个整数之间输出一个空格。

数据范围：
1 <= n <= 50
1 <= q <= 50
你可以把数组容量开到 200，保证足够容纳所有元素。
所有整数都在 int 范围内。
*/

#include <stdio.h>

int main(void) {
    int n;
    scanf("%d", &n);

    int arr[200];              // 按题意开到 200
    for (int i = 0; i < n; i++) {
        scanf("%d", &arr[i]);  // 初始有序序列
    }

    int q;
    scanf("%d", &q);           // 插入次数

    int len = n;               // 当前有效长度
    for (int t = 0; t < q; t++) {
        int x;
        scanf("%d", &x);       // 要插入的一个元素

        // 从前往后找第一个 > x 的位置
        int pos = 0;
        while (pos < len && arr[pos] <= x) {
            pos++;
        }

        // 从后往前挪动，为 x 腾出位置
        for (int j = len; j > pos; j--) {
            arr[j] = arr[j - 1];
        }

        arr[pos] = x;
        len++;
    }

    // 输出结果
    for (int i = 0; i < len; i++) {
        if (i > 0) {
            printf(" ");
        }
        printf("%d", arr[i]);
    }

    return 0;
}

