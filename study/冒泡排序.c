#include <stdio.h>

/*
 * 冒泡排序练习：
 *   读入 10 个整数，用冒泡排序从小到大排序，然后按顺序输出。
 *
 * 冒泡排序核心思想：
 *   - 外层循环控制“第几趟”，一共需要 n-1 趟。
 *   - 内层循环在当前还没排好序的区间里，
 *     依次比较相邻的两个数，如果前面的大，就交换。
 *   - 每一趟结束时，最大的那个数会被“冒”到这一趟的最后面。
 */

int main(void) {
    int a[10];

    /* 读入 10 个整数 */
    for (int i = 0; i < 10; i++) {
        if (scanf("%d", &a[i]) != 1) {
            return 0;
        }
    }

    int n = 10;  /* 数组长度 */

    /*
     * 外层循环：执行 n-1 趟冒泡
     * i 表示“已经完成了多少趟”，或者说“后面已经有 i 个最大值就位了”。
     */
    for (int i = 0; i < n - 1; i++) {
        /*
         * 内层循环：在还没排好的区间 [0, n-1-i] 内冒泡
         * 每次比较 a[j] 和 a[j+1]，如果前面的大，就交换位置。
         */
        for (int j = 0; j < n - 1 - i; j++) {
            if (a[j] > a[j + 1]) {
                int tmp = a[j];
                a[j] = a[j + 1];
                a[j + 1] = tmp;
            }
        }
    }

    /* 输出排序后的结果 */
    for (int i = 0; i < n; i++) {
        printf("%d ", a[i]);
    }
    printf("\n");

    return 0;
}

