#include <stdio.h>

int main(void) {
    /*
     * 题意：计算 S(n) = a + aa + aaa + ...（共 n 项）。
     * 输入：两个非负整数 a 和 n（a <= 9，n <= 8）。
     * 输出：S(n) 的值。
     *
     * 思路解释（为何这么写）：
     *   - 第 i 项是把数字 a 重复 i 次组成的数，比如 a=2 时：2、22、222...
     *   - 观察构造方法：
     *       2     = 0 * 10 + 2
     *       22    = 2 * 10 + 2
     *       222   = 22 * 10 + 2
     *     每一项都可以由“上一项 × 10，再加 a”得到，这正对应题目给的提示：
     *       2×10+2，22×10+2，...
     *   - 因此用一个变量 term 表示“当前这一项”，每次循环：term = term*10 + a；
     *     再把它累加到总和 sum 中即可。
     *   - 由于 n<=8，即使 a=9，最大一项为 99,999,999，8 项之和 < 8e8，
     *     int 也能装下；这里为了稳妥，使用 long long。
     */

    int a, n;
    if (scanf("%d %d", &a, &n) != 2) return 0; // 简单健壮性：读取失败直接结束

    long long sum = 0;      // 保存 S(n)
    long long term = 0;     // 保存当前项（a、aa、aaa ...）

    for (int i = 0; i < n; ++i) {
        term = term * 10 + a; // 由上一项左移一位（*10）再加上 a 得到下一项
        sum += term;          // 累加到总和
    }

    printf("%lld", sum);
    return 0;
}

