#include <stdio.h>

/*
思路要点（摘要）：
- 输入与目标：读取 X、N；从严格大于 X 的数开始找前 N 个质数并输出。
- 判素数策略：试除法；排除 <2 与偶数；仅试奇数因子至 d*d > n。
- 搜索流程：y 从 X+1 递增；遇质数就输出并累计，直到 found == N。
- 输出格式控制：行内空格分隔；每行 5 个换行；最后一行未满也补换行。
- 变量职责：found 统计已输出个数；y 当前被检查的数；col 当前行计数。

目的：
  从标准输入读取两个整数 X 和 N，输出“严格大于 X 的前 N 个质数（素数）”。

输出格式：
  - 同一行用空格分隔；
  - 每行最多输出 5 个数，满 5 个后换行；
  - 最后一行若未满 5 个，程序结束时也会补一个换行，保证整洁。
*/

static int is_prime(int n) {
    // 小于 2 的数不是质数
    if (n < 2) return 0;
    // 2 是最小且唯一的偶质数
    if (n == 2) return 1;
    // 除 2 外的偶数都不是质数
    if (n % 2 == 0) return 0;
    // 只试奇数因子，直到 d*d > n（即 d > sqrt(n)）
    for (int d = 3; d * d <= n; d += 2) {
        if (n % d == 0) return 0; // 有整除因子则不是质数
    }
    return 1; // 未发现因子，判定为质数
}

int main(void) {
    int X, N; // X：下界；N：需要输出的质数个数
    // 读取 X 和 N，若输入无效（不是两个整数），直接结束
    if (scanf("%d %d", &X, &N) != 2) return 0;

    int found = 0;      // 已找到的质数个数
    int y = X + 1;      // 从严格大于 X 的第一个整数开始检查
    int col = 0;        // 当前行已输出的数量（用于每行最多 5 个）

    while (found < N) {
        if (is_prime(y)) {         // 若 y 为质数，按格式输出
            if (col > 0) putchar(' '); // 行内不是第一个数时，先输出空格分隔
            printf("%d", y);
            found++;               // 总计数 +1
            col++;                 // 行内计数 +1
            if (col == 5) {        // 满 5 个则换行并重置行内计数
                putchar('\n');
                col = 0;
            }
        }
        y++; // 检查下一个整数
    }
    if (col != 0) putchar('\n');  // 收尾：最后一行未满也补换行
    return 0;
}
    
